package sintaxe_variaveis_e_fluxo;

public class EjemploFor {
	
	public static void main(String[] args) {
	    
		for (int contador = 0; contador <= 10; contador++) {
			// Inicio del bucle for
			// Inicialización del contador en 0
			// Condición de continuación: el contador es menor o igual a 10
			// Actualización del contador después de cada iteración: incremento en 1
			
			System.out.println(contador);  // Imprime el valor actual del contador
		}
		// Fin del bucle for
		
	}  // Fin del método main
	
}  // Fin de la clase EjemploFor
