package sintaxe_variaveis_e_fluxo;

public class EjemploWhile {

	public static void main(String[] args) {

		int contador = 0;  // Declara una variable entera llamada contador y le asigna el valor 0

		while (contador <= 10) {  // Mientras el valor de contador sea menor o igual a 10
			contador++;  // Incrementa el valor de contador en 1
			System.out.println(contador);  // Imprime el valor actual de contador
		}

	}
}
